            <p class="fs-6 fw-lighter">Com o objetivo de conduzir, delimitar e organizar a execução da profissão judiciária, balizado em princípios foi elaborado este Código de Ética, que visa orientar as ações do profissional em questão trazendo para o mais próximo de uma equidade justa possível. Dos objetivos tratados, tem-se o de proteger o profissional bem como os usufruintes desse ofício, de esclarecer procedências em determinadas situações e ainda predeterminar penalidades aos não cumprimentos ou violações das normas presentes nesse documento. É dever dos órgãos de fiscalização a divulgação deste documento para fiel observância do exercício da judicatura.</p>
            <h4 id="cap1">Capítulo I - DISPOSIÇÕES GERAIS</h4>
            <p>⚖ Art. 1 Não é permitido ingressar no Poder Judiciário estando com ficha criminal na polícia.</p>
            <p>⚖ Art. 2 É vedado o desvio de recursos material ou imaterial pertencentes ao poder judiciário. O desvio se aplica também a utilização de bens em sua posse para uso pessoal.
            </p>
            <p>⚖ Art. 3 Todas as alterações e aplicações devem ser postadas no diário respectivo para fins de conhecimento público. Quaisquer alterações só são válidas após publicação no diário oficial.
            </p>
            <p>⚖ Art. 4 É proibido o vazamento de informações sigilosas, especialmente processos que ocorrem em segredo de justiça.</p>
           <p>⚖ Art. 5 É vedada a postulação de processos a desfavor da Prefeitura.</p>
           <p>⚖ Art. 6 Deve-se obedecer a hierarquia interna. Todos aqueles que exercem função jurídica devem colaborar com os órgãos de aferição e controle na execução do seu trabalho.</p>

            <h4 id="cap2">Capítulo II - DA ADVOCACIA
            </h4>
            <p>⚖ Art. 7 As atividades da advocacia são:</p>
            <ul class="list-unstyled">
                <li>I - a abertura de processos jurídicos;    
                </li>
                <li>II - as atividades de consultoria, assessoria e direção jurídicas;
                </li>
                <li>III - as atividades de documentação e registros públicos.
                </li>
            </ul>
            <p>⚖ Art. 8 O advogado, em primeira instância, além de ser defensor do estado democrático, é indispensável para a administração da justiça, o mesmo ainda se faz presente preservador da paz social. Por tanto são deveres do mesmo:

            </p>
            <ul class="list-unstyled">
              <li>I -  preservar, de modo profissional, sua conduta, nobreza, ordem e dignidade profissionais no momento em que zelar, essencialmente, pelo seu caráter e indispensabilidade; 

              </li>
              <li>II - procurar aperfeiçoamento e empenho em sua função;</li>
              <li>III - contribuir de toda e qualquer forma, para o cumprimento das leis e dos direitos existentes em caso;
              </li>
              <li>IV - encorajar uma conversa entre litigantes com a finalidade de resolver quaisquer diferenças e mitigar instauração de novos litígios, além de aconselhar o cliente a não entrar em uma aventura judicial.
              </li>

            </ul>
            <p>⚖ Art. 9 O advogado não deverá aceitar como cliente alguém que já tenha iniciado tal processo com outro advogado, sem antes a informação e interação prévia por parte do cliente, salvo com ordens do juiz.
            </p>
            <p>⚖ Art. 10 É direito e dever de um profissional de direito, como sendo advogado, aceitar a defesa criminal, desconsiderando sua opinião pessoal sobre o caso.</p>
            <p>⚖ Art. 11 O sigilo profissional acontece, independentemente da profissão, deve ser respeitado, salvo quando o profissional advogado receber ameaças e atentados contra a sua vida, sua honra, ou quando o mesmo se ver afrontado pelo próprio cliente, que em defesa pessoal, poderá revelar segredos, considerando a restrição ao interesse da causa.</p>

            <h4 id="cap3">Capítulo III - DO PODER JUDICIÁRIO
            </h4>
            <p>⚖ Art. 12 O ingresso na carreira da magistratura inicia-se através de concurso público do tribunal de justiça.</p>
            <p>⚖ Art. 13 São deveres do magistrado: </p>
            <ul class="list-unstyled">
              <li>I - Cumprir suas funções com serenidade, exatidão e independência;
              </li>
              <li>II - determinar as providências necessárias para que os atos processuais se realizem nos prazos legais;
              </li>
              <li>III -  exercer assídua fiscalização sobre os subordinados, especialmente no que se refere à cobrança de custas e emolumentos, embora não haja reclamação das partes.
              </li>
            </ul>
            <p>⚖ Art. 14 É vedado ao judiciário:</p>
            <ul class="list-unstyled">
              <li>I - Dar declarações ou manifestações sobre processos pendentes;
              </li>
              <li>II - Assumir casos em que seja tratado como réu ou alvo principal.
              </li>
            </ul>
            <p>⚖ Art. 15 O Magistrado irá responder por perdas ou danos quando:
            </p>
            <ul class="list-unstyled">
              <li>I - prosseguir com fraude no exercício da função;
              </li>
              <li>II - recusar, omitir ou retardar sem motivo providência que deva ordenar o ofício, ou o requerimento das partes.
              </li>
            </ul>
            <p>⚖ Art. 16 O exercício da magistratura exige conduta compatível com os preceitos deste Código e pelos princípios da independência, da imparcialidade, do conhecimento e capacitação, da cortesia, da transparência, do segredo profissional, da prudência, da diligência, da integridade profissional e pessoal, da dignidade, da honra e do decoro.
            </p>
            <p>⚖ Art. 17 A atividade judicial deve desenvolver-se de modo a garantir e fomentar a dignidade da pessoa humana, objetivando assegurar e promover a solidariedade e a justiça na relação entre as pessoas.
            </p>
            <p>⚖ Art. 18 Exige-se do magistrado que seja eticamente independente e que não interfira, de qualquer modo, na atuação jurisdicional de outro colega, exceto em respeito às normas legais.
            </p>
            <p>⚖ Art. 19 Impõe-se ao magistrado pautar-se no desempenho de suas atividades sem receber indevidas influências externas e estranhas à justa convicção que deve formar para a solução dos casos que lhe sejam submetidos.
            </p>
            <p>⚖ Art. 20 O magistrado imparcial é aquele que busca nas provas a verdade dos fatos, com objetividade e fundamento, mantendo ao longo de todo o processo uma distância equivalente das partes, e evita todo o tipo de comportamento que possa refletir favoritismo, predisposição ou preconceito.
            </p>
            <p>⚖ Art. 21 Não se considera tratamento discriminatório injustificado: 
            </p>
            <ul class="list-unstyled"><li>I - a audiência concedida a apenas uma das partes ou seu advogado, contanto que se assegure igual direito à parte contrária, caso seja solicitado;
            </li>
          <li>II - o tratamento diferenciado resultante de lei.
          </li></ul>
          <p>⚖ Art. 22 A atuação do magistrado deve ser transparente, documentando-se seus atos, sempre que possível, mesmo quando não legalmente previsto, de modo a favorecer sua publicidade, exceto nos casos de sigilo contemplado em lei.</p>
          <p>⚖ Art. 23 O magistrado, obedecido o segredo de justiça, tem o dever de informar ou mandar informar aos interessados acerca dos processos sob sua responsabilidade, de forma útil, compreensível e clara.
          </p>
          <p>⚖ Art. 24 É dever do magistrado recusar benefícios ou vantagens de ente público, de empresa privada ou de pessoa física que possam comprometer sua independência funcional.</p>
          <p>⚖ Art. 25 O magistrado deve manter atitude aberta e paciente para receber argumentos ou críticas lançados de forma cortês e respeitosa, podendo confirmar ou retificar posições anteriormente assumidas nos processos em que atua.</p>
            <h4 id="cap4">Capítulo IV - DOS PODERES</h4>
            <p>⚖ Art. 26 O Tribunal de Justiça é constituído da seguinte forma hierárquica:</p>
            <ul class="list-unstyled">
              <li>Presidente</li>
              <li>Desembargador</li>
              <li>Juiz</li>
              <li>Promotor</li>
              <li>Diretor</li>
              <li>Conselheiro</li>
              <li>Advogado Master</li>
              <li>Advogado Sênior</li>
              <li>Advogado Pleno</li>
              <li>Advogado Júnior</li>
              <li>Defensor Público</li>
              <li>Estagiário</li>
            </ul>
            <p>⚖ Art. 27 Funções de cada cargo:</p>
            <ul class="list-unstyled">
              <li>Presidente: gestão de todo o judiciário.</li>
              <li>Desembargador: despacho de sentenças em 2º instância, supervisão, observância das leis.
              </li>
              <li>Juiz de Direito: expedição de documentos, despacho de sentenças em 1º instância, supervisão da advocacia.
              </li>
              <li>Promotor: instauração de inquéritos, investigação, observância na aplicação do código penal</li>
              <li>Diretoria: gestão de pessoas, supervisão da comissão de exame de  ordem.
              </li>
              <li>Conselho: consulta, atendimento, expedição de documentos, supervisão de advogados, aplicação de concursos, aplicação de cursos, admissão.
              </li>
              <li>Advogado: realização de consulta, atendimento, expedição de documentos.
              </li>
              <li>Estagiário: auxiliar os demais membros do judiciário na consultoria através de acompanhamento, postagem de relatório entre outros serviços solicitados pelos mesmos.
              </li>
        
            </ul>
            <p>⚖ Art. 28 Advogado Júnior não poderá assumir processos sozinhos, sendo obrigatório o acompanhamento por um Advogado Sênior, salvo casos com autorização do Conselho.</p>
            <p>⚖ Art. 29 Só poderá ter organizações como cliente os que estiverem no cargo de Advogado Sênior ou acima.</p>
            <p>⚖ Art. 30 Aqueles que trabalham no judiciário devem exercer de forma independente, não podendo possuir outro vínculo empregatício.
            </p>
            <h4 id="cap5">Capítulo V - DA CONDUTA
            </h4>
            <p>⚖ Art. 31 Durante o exercício da função é obrigatório a utilização de roupas sociais e em audiências é obrigatório o uso de terno.</p>
            <p>⚖ Art. 32 Aqueles que exercem cargos jurídicos representam o judiciário, devendo manter uma boa postura e aparência.</p>
            <p>⚖ Art. 33 Obrigatório o uso do crachá (MASCULINO:215 / FEMININO: 198) durante representações judiciais.
            </p>
